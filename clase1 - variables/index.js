/**
 * Variables
 */

// hay tres tipos de variables
// var, let, const

// var
// sintaxis antigua
var variable1 = 'value'

// let
// sintaxis moderna (es la que usamos)
let variable2 = 'value'

// const
// no se puede reasignar, osea no se puede cambiar el valor de una constante
const constante = 'value'

/**
 * Tipos de Datos
 */
// string
let string1 = 'hola'

// number
let number1 = 10

// boolean
let boolean1 = true

// undefined
let undefined1

// null
let null1 = null

// array
let array = [1, 2, 3]

// objeto
let objeto = { name: 'jm', age: 30 }

console.log(string1)
console.log(number1)
console.log(boolean1)
console.log(undefined1)
console.log(null1)
console.log(array)
console.log(objeto)
